x = input('Please enter a number: ')
y = input('Please enter a second number: ')
z = input('Please enter a third number: ')
x=int(x)
y=int(y)
z=int(z)
print(f'{str(x)} + {str(y)} + {str(z)} = {x+y+z}')
print(f'{str(x)} - {str(y)} = {x-y}')
print(f'{str(z)} * {str(x)} = {z*x}')
print(f'({str(x)} + {str(y)} + {str(z)}) / {str(z)} = {(x+y+z)/z}')